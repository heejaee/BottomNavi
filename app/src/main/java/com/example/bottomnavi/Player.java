package com.example.bottomnavi;

public class Player {
    private String profile;
    private String age;
    private String apperances;
    private String total_goal;
    private String name;
    private String country;
    private String position;

    public Player(){}

    public String getPosition() {
        return position;
    }

    public void setPosition(String position) {
        this.position = position;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getProfile() {
        return profile;
    }

    public void setProfile(String profile) {
        this.profile = profile;
    }


    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }


    public String getApperances() {
        return apperances;
    }

    public void setApperances(String apperances) {
        this.apperances = apperances;
    }


    public String getTotal_goal() {
        return total_goal;
    }

    public void setTotal_goal(String total_goal) {
        this.total_goal = total_goal;
    }


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
